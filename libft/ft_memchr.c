#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*a;
	size_t			i;

	i = 0;
	a = (unsigned char *)s;
	while (n--)
		if (*a++ == (unsigned char)c)
			return (a - 1);
	return (NULL);
}
